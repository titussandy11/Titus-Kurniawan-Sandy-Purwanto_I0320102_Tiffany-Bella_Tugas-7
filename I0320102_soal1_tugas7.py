#Fungsi capitalize
teks = 'titus kurniawan'
ucapan = 'selamat siang'
flag = teks.capitalize()
print(ucapan + ''+ flag)

#Fungsi center
teks = 'Selamat datang di Kota Surakarta'
center = teks.center(70, '-')
print(center)

#Fungsi count
teks = 'Selamat sore dan selamat datang di Indiemart, silahkan buah segarnya, silahkan rotinya'
count = (teks.count('silahkan'))
print(count)

#Fungsi replace
teks = 'Selamat sore dan selamat datang di Indiemart, silahkan buah segarnya, silahkan rotinya'
replace = teks.replace('sore', 'malam')
print(replace)

